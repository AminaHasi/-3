# 6.Логічній змінній t присвоїти значення true або false залежно від того, є натуральне число k
# ступенем 3 чи ні.

k = float(input("Введіть натуральне число k: "))

while k != 1:
    t = True
    if k % 3 == 0:
        print(t)
        break
    else:
        t = False
        print(t)
        break
